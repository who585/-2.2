class Book:
    def __init__(self, name: str, id: int, pages: int):
        self.name = name
        self.id = id
        self.pages = pages

    def __str__(self):
        return f'Книга "{self.name}"'

    def __repr__(self):
        return f"Book(id_={self.id}, name={self.name!r}, pages={self.pages})"
